    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        JFileChooser fc = new JFileChooser(new File("C:\\Users\\ASUS\\Desktop"));
        fc.setDialogTitle("Select an Image");
        fc.addChoosableFileFilter(new FileNameExtensionFilter("*.Images", "jpg", "png", "gif", "tif"));
        fc.setAcceptAllFileFilterUsed(false);
        int result = fc.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {

            File file = fc.getSelectedFile();
            String path = file.getAbsolutePath();
            jLabel_image.setIcon(resizeImage(path, null));
            imgPath = path;

        } else if (result == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "No Image Selected");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (imgPath != null) {
            try {
                InputStream is = new FileInputStream(imgPath);
                String sql = "UPDATE new_book SET book_image = ?, book_name = ?, department = ?, edition = ?, writer = ?, pages = ?, price = ? WHERE book_id = ?";

                preparedStatement = connection.prepareStatement(sql);
               
                preparedStatement.setBlob(1, is);
                preparedStatement.setString(2, jTextField2.getText());
                preparedStatement.setString(3, (String) jComboBox1.getSelectedItem());
                preparedStatement.setString(4, (String) jComboBox2.getSelectedItem());
                preparedStatement.setString(5, jTextField5.getText());
                preparedStatement.setString(6, jTextField6.getText());
                preparedStatement.setString(7, jTextField7.getText());
                preparedStatement.setString(8, jTextField1.getText());
                
                if(preparedStatement.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(null, "Data Updated Successfully");
                }else{
                    JOptionPane.showMessageDialog(null, "No Image Found");
                }

            } catch (FileNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "No Image Selected");
        }
        imgPath = null;
        
    }//GEN-LAST:event_jButton2ActionPerformed

