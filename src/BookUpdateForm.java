    public ImageIcon resizeImage(String imagePath, byte[] pic) {

        ImageIcon myImage = null;

        if (imagePath != null) {
            myImage = new ImageIcon(imagePath);
        } else {
            myImage = new ImageIcon(pic);
        }
        Image img = myImage.getImage();
        Image resizedImage = img.getScaledInstance(jLabel_image.getWidth(), jLabel_image.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(resizedImage);

        return image;
    }

    public void setDataToTheField(String id) {
        try {
            String sql = "SELECT * FROM book_list WHERE book_id ='" + id + "' ";

            preparedStatement = connection.prepareStatement(sql);
            resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {

                jTextField1.setText(String.valueOf(resultSet.getInt("book_id")));
                jTextField2.setText(resultSet.getString("book_name"));

                byte[] byteImage = resultSet.getBytes("book_image");
                ImageIcon ii = new ImageIcon(byteImage);
                Image img = ii.getImage();
                Image resizedImage = img.getScaledInstance(jLabel_image.getWidth(), jLabel_image.getHeight(), Image.SCALE_SMOOTH);
                jLabel_image.setIcon(new ImageIcon(resizedImage));

                jComboBox1.setSelectedItem(resultSet.getString("department"));
                jComboBox2.setSelectedItem(resultSet.getString("edition"));
                jTextField5.setText(resultSet.getString("writer"));

                jTextField6.setText(resultSet.getString("pages"));
                jTextField7.setText(resultSet.getString("price"));

            }
        } catch (SQLException ex) {
            Logger.getLogger(BookUpdateForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

