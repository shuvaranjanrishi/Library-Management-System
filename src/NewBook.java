    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String sql = "INSERT INTO new_book(book_id, book_image, book_name, department, edition, writer, pages, price) VALUES(?,?,?,?,?,?,?,?)";
        try {

            File f = new File(imgPath);
            FileInputStream img = new FileInputStream(new File(imgPath));
            int len = (int) f.length();

            pst = con.prepareStatement(sql);

            pst.setString(1, jTextField1.getText());
            pst.setBinaryStream(2, img, len);
            pst.setString(3, jTextField2.getText());
            pst.setString(4, (String) jComboBox1.getSelectedItem());
            pst.setString(5, (String) jComboBox2.getSelectedItem());
            pst.setString(6, jTextField4.getText());
            pst.setString(7, jTextField5.getText());
            pst.setString(8, jTextField6.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "New Book Added");
            clearAll();

        } catch (HeadlessException | FileNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

